    private void EnregistrerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EnregistrerMouseClicked
        if(PNameTb.getText().isEmpty() || PassNumTb.getText().isEmpty() || PAdressTb.getText().isEmpty() || PPhoneTb.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(this,"Missing Information");
            }
         else
            {
                try{
                    CountPassengers();
                    Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airlinedb","root","0000");
                    PreparedStatement Add = Con.prepareStatement("insert into PassengersTbl values(?,?,?,?,?,?,?)");
                    Add.setInt(1, PassId);
                    Add.setString(2, PNameTb.getText());
                    Add.setString(3, NatCb.getSelectedItem().toString());
                    Add.setString(4, GenCb.getSelectedItem().toString());
                    Add.setString(5, PassNumTb.getText());
                    Add.setString(6, PAdressTb.getText());
                    Add.setString(7, PPhoneTb.getText());
                    int row = Add.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Passenger Added");
                    Con.close();
                    DisplayPassengers();
                    Clear();
                }catch (Exception e){
                    JOptionPane.showMessageDialog(this, e);

                }
            }
    }//GEN-LAST:event_EnregistrerMouseClicked

